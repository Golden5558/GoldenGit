void	ft_init_coord(char **argv, int coord[4][4])
{
	int	i;

	i = 0;
	while (i <= 3)
	{
		coord[0][i] = argv[1][i * 2] - '0';
		coord[1][i] = argv[1][8 + (i * 2)] - '0';
		coord[2][i] = argv[1][16 + (i * 2)] - '0';
		coord[3][i] = argv[1][24 + (i * 2)] - '0';
		i++;
	}
}
