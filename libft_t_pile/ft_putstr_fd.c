#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (!s)
		return ;
	write(fd, s, ft_strlen(s));
}

void	ft_fputstr(FILE *file, char *s)
{
	if (!s)
		return ;
	fwrite(s, 1, ft_strlen(s), file);
}
